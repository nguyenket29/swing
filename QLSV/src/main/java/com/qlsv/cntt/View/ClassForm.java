    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        StringBuilder builder = new StringBuilder();
        Validator.validatorEmpty(txtMaLop, builder, "Mã lớp không được để trống");

        if (builder.length() > 0) {
            MesageHepler.showErrorDialog(menuForm, builder.toString(), "Lỗi !");
            return;
        }

        try {
            Lop sj = new Lop();
            sj.setMaLop(txtMaLop.getText());
            sj.setTenLop(txtTenLop.getText());
            sj.setMaKhoa(txtMaKhoa.getText());
            CLassDAO dao = new CLassDAO();
            if (dao.save(sj)) {
                MesageHepler.showMessageDialog(menuForm, "Lớp học Đã Được Thêm", "Thông báo !");
            } else {
                MesageHepler.showConfirmDialog(menuForm, "Lớp học chưa được thêm", "Cảnh báo !");
            }
            showAll();
        } catch (Exception e) {
            e.printStackTrace();
            MesageHepler.showErrorDialog(menuForm, e.getMessage(), "Lỗi !");
        }
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        if (tbClasses.getSelectedRow() == -1) {
            MesageHepler.showMessageDialog(menuForm, "Hãy chọn một dòng để xóa", "Thông báo !");
        } else if (tbClasses.getSelectedRowCount() == 0) {
            MesageHepler.showMessageDialog(menuForm, "Không có dữ liệu để xóa", "Thông báo !");
        } else {
            try {
                String msv = tbClasses.getModel()
                        .getValueAt(tbClasses.getSelectedRow(), 0).toString();
                CLassDAO dao = new CLassDAO();
                if (dao.delete(msv)) {
                    MesageHepler.showMessageDialog(menuForm, "Xóa thành công", "Thông báo !");
                    showAll();
                } else {
                    MesageHepler.showMessageDialog(menuForm, "Xóa thất bại", "Cảnh báo !");
                }
            } catch (Exception ex) {
                Logger.getLogger(CoreForm.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        StringBuilder builder = new StringBuilder();
        Validator.validatorEmpty(txtMaLop, builder, "Mã lớp không được để trống");

        if (builder.length() > 0) {
            MesageHepler.showErrorDialog(menuForm, builder.toString(), "Lỗi !");
            return;
        }

        if (JOptionPane.showConfirmDialog(this, "Xác nhận cập nhật lớp !")
                == JOptionPane.NO_OPTION) {
            return;
        }

        Lop sj = new Lop();
        sj.setMaLop(txtMaLop.getText());
        sj.setTenLop(txtTenLop.getText());
        sj.setMaKhoa(txtMaKhoa.getText());
        CLassDAO dao = new CLassDAO();
        try {
            if (dao.update(sj)) {
                MesageHepler.showMessageDialog(menuForm, "Lớp Đã Được Chỉnh Sửa", "Thông báo !");
            } else {
                MesageHepler.showConfirmDialog(menuForm, "Lớp chưa được chỉnh sửa", "Cảnh báo !");
            }
            showAll();
        } catch (Exception ex) {
            Logger.getLogger(SubjectsForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSuaActionPerformed

